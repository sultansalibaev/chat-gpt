import { langIs } from '@/use/utils/langIs'


function getLangIndex() {
    if (langIs("en")) return 0;
    else if (langIs("kz")) return 1;
}

const i18n = (text) => {
    if (text == undefined) {
        //console.log('i18n - text = ', text);
        return text;
    }
    text = text + "";
    if (_i18n[text.trim()] == undefined) return text;
    return langIs("ru") ? text.trim() : _i18n[text.trim()][getLangIndex()];
};

export default i18n;